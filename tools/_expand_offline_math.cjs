function makeQ(id, topic, type, prompt, answer, steps, cm) {
  return {
    id, grade: 5, topic, type,
    params: {},
    prompt, answer,
    teacherSteps: steps.map((s, i) => ({
      k: i === 0 ? 'concept' : i === 1 ? 'formula' : 'compute',
      say: s
    })),
    source: 'generated',
    meta: {},
    question: prompt,
    hints: [
      steps[0],
      steps[1] || steps[0],
      '📐 一步步算：\n' + steps.slice(2).map((s, i) => `① ${s}`).join('\n') + '\n算完記得回頭檢查喔！✅',
      '👉 ' + (cm[0] || '做完後回頭檢查一次。')
    ],
    steps: steps.slice(2),
    explanation: `${prompt} → ${answer}`,
    kind: 'general',
    common_mistakes: cm
  };
}

